package ex01.io;

public class program {

	public static void main(String[] args) {
		System.out.write('3');
		
		System.out.println("");
		
		System.out.write(51);
		
		System.out.println("");
		
		System.out.write('3'+1); // 51+1
		
		System.out.println("");
		
		System.out.write('A');
		
		System.out.println("");
		
		System.out.write('A'+1); // B
		System.out.flush();
	}

}
